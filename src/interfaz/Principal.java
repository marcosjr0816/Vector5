    private void cmdLlenarManualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLlenarManualActionPerformed
        double n;
        txtElementos.setText("");
        txtSumatoria.setText("");
        for (int i = 0; i < v.length; i++) {
            n=Double.parseDouble(JOptionPane.showInputDialog(this,"Digite el elemento en la posiciÃ³n " + i));
            v[i]=n;
        }
        for (int j = 0; j < v.length; j++) {
            txtElementos.append(v[j]+"\n");
        }
    }//GEN-LAST:event_cmdLlenarManualActionPerformed

    private void cmdLlenarAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdLlenarAutoActionPerformed
        double n;
        for (int i = 0; i < v.length; i++) {
            n=(int)(Math.random()* 100 + 1);
            v[i]=n;
        }
        txtElementos.setText("");
        txtSumatoria.setText("");
        ;
        JOptionPane.showMessageDialog(this,"Vector llenado automaticamente");
        for (int j = 0; j < v.length; j++) {
            txtElementos.append(v[j]+"\n");
        }
    }//GEN-LAST:event_cmdLlenarAutoActionPerformed

    private void cmdMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdMostrarActionPerformed
        double sum=0,prod=1;
       int mitad;
       mitad=v.length/2;
        for (int i = 0; i < mitad; i++) {
           prod=prod*v[i];
            
        }
        for (int i = mitad; i < v.length; i++) {
            sum=sum+v[i];
            
        }
        txtSumatoria.setText(String.valueOf(sum));
        txtProductoria.setText(String.valueOf(prod));
    }//GEN-LAST:event_cmdMostrarActionPerformed

